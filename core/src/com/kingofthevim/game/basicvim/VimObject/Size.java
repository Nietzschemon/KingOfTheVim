package com.kingofthevim.game.basicvim.VimObject;

public class Size {


    private int width = 22;
    private int height = 44;

    public Size(int width, int height){
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

}
